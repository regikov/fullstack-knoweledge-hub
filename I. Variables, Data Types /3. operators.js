// *** ARITHMETIC OPERATORS ***

let x;

x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 7 % 5; // It is remainder so 2, be careful 

// Concatenation (aka the + sign, puts 2 things together)
x = 'Hello' + ' ' + 'World';

// Exponent (aka the power of, so 8)
x = 2 ** 3;

// Increment (aka increasing by 1)
x = 1;
x++;

// Decrement
x = 2
x--;



// *** Assignment Operators ***

x = 10;

x += 5; // aka x= 10+5 (so 15)
x -= 5; // aka x= 15-5 (so 10)
x *= 5; // aka x= 10 * 5 (so 50)
x /= 5; // aka x= 50/5 (so 10)
x %= 5; // aka x=10%5 (so 0)
x **= 5; // aka x= 0 on the power of 5 (so 0)



// *** COMPARISON OPERATORS *** 


// Equal to (Just the value, not the type)
x = 2 == '2';

// Equal to (Type and value)
x = 2 === '2';

// Not equal to (Just the value, not the type)
x = 2 != '2';

// Not equal to (Type and value)
x = 2 !== 2;

// Greater than and less than
x = 10 > 5;
x = 10 < 5;
x = 10 <= 5;
x = 10 >= 5;


// *** LOGICAL OPERATORS *** 

// && (aka AND)
let a = 10 
let b = 11 
console.log(a<20 && b<30); // it is true here 

// || (aka OR)
let c = 10 
let d = 11 
console.log(c<20 || d<10); // it is true here 

